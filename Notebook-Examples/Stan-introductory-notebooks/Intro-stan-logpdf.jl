function loglik(x)
  ll = 0.0
  ll += log(pdf(Normal(178, 20), x[1]))
  ll += log(pdf(Uniform(0, 50), x[2]))
  ll += sum(logpdf.(Normal(x[1], x[2]), df2.height))
  -ll
end

