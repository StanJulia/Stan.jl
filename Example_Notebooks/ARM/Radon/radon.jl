function mslu(data, PI = (0.055, 0.945))
    m = mean.(eachcol(data))
	s = std.(eachcol(data))
    lower = quantile.(eachcol(data), PI[1])
    upper = quantile.(eachcol(data), PI[2])
    return (mean = m,
